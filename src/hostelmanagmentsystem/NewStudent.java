    public void roomNumber()
    {
        int i=0;
        try
        {
            Connection con=Connectionprovider.getCon();
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery("select *from room where activate='Yes' and roomStatus='Not Booked'");
            while(rs.next())
            {
              i=1; // if room exist for booking
              jComboBox1.addItem(rs.getString(1)); //available rooms
                       
           }
            if(i==0) //if room does not exists
            {
                jButton2.setVisible(false);
                JOptionPane.showMessageDialog(null,"all rooms are already booked");
                setVisible(false); //closing form
            }
          }
        
        catch(Exception e)
          {
              JOptionPane.showMessageDialog(null,e);
          }
    }
    

