    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String mobileNo= jTextField1.getText();
        String name= jTextField2.getText();
        String fathername= jTextField3.getText();
        String mothername= jTextField4.getText();
        String email= jTextField5.getText();
        String address= jTextField6.getText();
        String college= jTextField7.getText();
        String adhar= jTextField8.getText();
        String roomnumber= (String)jComboBox1.getSelectedItem(); //selected room by user
        String status="living"; //living in hostel
      
        try
        {
            Connection con=Connectionprovider.getCon();
            PreparedStatement ps=con.prepareStatement("insert into student values(?,?,?,?,?,?,?,?,?,?)"); //for 10 values
            ps.setString(1, mobileNo);
            ps.setString(2, name);
            ps.setString(3, fathername);
            ps.setString(4, mothername);
            ps.setString(5, email);
            ps.setString(6,address );
            ps.setString(7, college);
            ps.setString(8, adhar);
            ps.setString(9, roomnumber);
            ps.setString(10, status);
            ps.executeUpdate();
              
            PreparedStatement ps1=con.prepareStatement("update room set roomStatus='Booked' where number=?"); //room booked
            ps1.setString(1, roomnumber);
            ps1.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"sucessfully updated");
            clear(); //to clear all fields
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }                                       

    }//GEN-LAST:event_jButton2ActionPerformed

